/*
===============================================================================
POST-DEPLOYMENT VERIFICATION QUERIES

Run these after executing the file.
===============================================================================

-- 1. Schemas
SELECT schema_name
FROM information_schema.schemata
WHERE schema_name IN ('sheep_dw', 'sheep_mart')
ORDER BY schema_name;


-- 2. Warehouse tables
SELECT
    table_schema,
    table_name
FROM information_schema.tables
WHERE table_schema IN ('sheep_dw', 'sheep_mart')
  AND table_type = 'BASE TABLE'
ORDER BY table_schema, table_name;


-- 3. Seeded lactation stages
SELECT *
FROM sheep_dw.dim_lactation_stage
ORDER BY stage_order;


-- 4. Seeded quality categories
SELECT *
FROM sheep_dw.dim_data_quality
ORDER BY data_quality_key;


-- 5. Foreign keys on the fact table
SELECT
    tc.constraint_name,
    kcu.column_name,
    ccu.table_schema AS referenced_schema,
    ccu.table_name AS referenced_table,
    ccu.column_name AS referenced_column
FROM information_schema.table_constraints AS tc
JOIN information_schema.key_column_usage AS kcu
    ON tc.constraint_name = kcu.constraint_name
   AND tc.constraint_schema = kcu.constraint_schema
JOIN information_schema.constraint_column_usage AS ccu
    ON ccu.constraint_name = tc.constraint_name
   AND ccu.constraint_schema = tc.constraint_schema
WHERE tc.constraint_type = 'FOREIGN KEY'
  AND tc.table_schema = 'sheep_dw'
